public class Calculator {


    public Calculator(){

    }

    public int addNum(int num1, int num2){
        return num1 + num2;

    }

    public int subNum(int num1, int num2) {
        return num1 - num2;
    }

    public double divideNum(double divNum1, double divNum2){
        return divNum1 / divNum2;
    }

    public int multiNum(int i, int i1) {
        return i * i1;
    }
}
